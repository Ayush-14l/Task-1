a = 10
b = 5

# Arithmetic Operations
print("Addition:", a + b)
print("Subtraction:", a - b)

# Comparison Operations
print("a == b:", a == b)
print("a != b:", a != b)
print("a < b:", a < b)
print("a > b:", a > b)

# Logical Operations
print("(a > 0 and b > 0):", a > 0 and b > 0)
print("(a > 0 or b < 0):", a > 0 or b < 0)